    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        try {
            if(txtRoleName.getText().trim().equals("")){
                JOptionPane.showConfirmDialog(this, bundle.getString("error.role.name.required"));
                return;
            }
            Roles role = new Roles();
            RolesHome dao = new RolesHome();
            role.setRoleName(txtRoleName.getText());
            dao.persist(role);
            dao.commit();
            RolesHome.close();
            rolesModel.addRow(role);
            clear();
        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            RolesHome.close();
        }
}

