    private void addUser() {
        try {
            if (validateUser()) {
                Users user = new Users();
                UsersHome dao = new UsersHome();
                user.setPassword(txtPassword.getText());
                user.setUserName(txtUserName.getText());
                user.setActive(chkActive.isSelected());
                dao.persist(user);
                dao.commit();
                UsersHome.close();
                usersModel.addRow(user);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        // users.add(user);
        clear();

    }

    private void updateUser() {
        try {
            if (validateUser()) {
                
                UsersHome dao = new UsersHome();
                user.setPassword(txtPassword.getText());
                user.setUserName(txtUserName.getText());
                user.setActive(chkActive.isSelected());
                dao.attachDirty(user);
                dao.commit();
                UsersHome.close();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        // users.add(user);
        clear();

    }

    private boolean validateUser() {
        if (txtUserName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, bundle.getString("error.username.required"));
            return false;
        } else if (txtPassword.getText().trim().equals("") || txtRePassword.getText().equals("")) {
            JOptionPane.showMessageDialog(this, bundle.getString("error.password.required"));
            return false;
        } else if (!txtPassword.getText().equals(txtRePassword.getText())) {
            JOptionPane.showMessageDialog(this, bundle.getString("error.password.identical"));
            return false;
        }
        return true;
    }

